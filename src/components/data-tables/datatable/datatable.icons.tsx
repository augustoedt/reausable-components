export function OrderComponent<T>({
  prop,
  filter,
  onAction,
}: {
  prop: keyof T;
  filter: OrderFilter<T>;
  onAction(action: string, item: undefined, prop: keyof T): void;
}) {
  const handleAction = () => {
    onAction("orderBy", undefined, prop);
  };

  const isCurrent = prop == filter.prop;
  if (filter.order == Order.asc && isCurrent) {
    return (
      <button onClick={handleAction}>
        <AscIcon />
      </button>
    );
  }
  if (filter.order == Order.desc && isCurrent) {
    return (
      <button onClick={handleAction}>
        <DescIcon />
      </button>
    );
  }
  return (
    <button onClick={handleAction}>
      <NoneIcon />
    </button>
  );
}
